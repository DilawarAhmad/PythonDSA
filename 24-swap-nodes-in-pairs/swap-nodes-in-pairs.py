# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def swapPairs(self, head: Optional[ListNode]) -> Optional[ListNode]:
        dummy = ListNode(0)
        dummy.next = head
        prev = dummy
        if not head or not head.next:
            return head
        while prev.next and prev.next.next:
            A = prev.next
            B = A.next
            A.next =B.next
            B.next = A
            prev.next = B
            prev = A
        return dummy.next